    def test_handle_none_for_pre_dolphin(self):
        """测试 handle_none 验证器 - pre_dolphin None 转换为 []"""
        config = AgentConfig(pre_dolphin=None)

        assert config.pre_dolphin == []

    def test_handle_none_for_post_dolphin(self):
        """测试 handle_none 验证器 - post_dolphin None 转换为 []"""
        config = AgentConfig(post_dolphin=None)

        assert config.post_dolphin == []

    def test_is_plan_mode_enabled(self):
        """测试 is_plan_mode 方法 - 启用状态"""
        config = AgentConfig(plan_mode={"is_enabled": True})

        assert config.is_plan_mode() is True

    def test_is_plan_mode_disabled(self):
        """测试 is_plan_mode 方法 - 禁用状态"""
        config = AgentConfig(plan_mode={"is_enabled": False})

        assert config.is_plan_mode() is False

    def test_is_plan_mode_none(self):
        """测试 is_plan_mode 方法 - plan_mode 为 None"""
        config = AgentConfig(plan_mode=None)

        # When plan_mode is None, is_plan_mode() returns None
        assert config.is_plan_mode() is None

    def test_is_plan_mode_missing_key(self):
        """测试 is_plan_mode 方法 - 缺少 is_enabled 键"""
        # Pydantic will validate that the dict value is a boolean
        # So we need to pass a valid boolean value for other_key
        config = AgentConfig(plan_mode={"other_key": True})

        # get() will return None for missing key, which is falsy
        assert config.is_plan_mode() is False or config.is_plan_mode() is None

    def test_append_task_plan_agent_with_plan_mode(self):
        """测试 append_task_plan_agent 方法 - plan mode 启用"""
        config = AgentConfig(plan_mode={"is_enabled": True})

        config.append_task_plan_agent()

        # Should add Task_Plan_Agent to skills.agents
        assert config.skills is not None
        assert config.skills.agents is not None
        assert len(config.skills.agents) > 0

        # Find the Task_Plan_Agent
        task_plan_agent = None
        for agent in config.skills.agents:
            if hasattr(agent, "agent_key") and agent.agent_key == "Task_Plan_Agent":
                task_plan_agent = agent
                break

        assert task_plan_agent is not None

    def test_append_task_plan_agent_without_plan_mode(self):
        """测试 append_task_plan_agent 方法 - plan mode 禁用"""
        config = AgentConfig(plan_mode={"is_enabled": False})

        config.append_task_plan_agent()

        # Should not add Task_Plan_Agent
        assert config.skills is not None
        # Should be empty since we didn't add any agents
        assert len(config.skills.agents) == 0

    def test_append_task_plan_agent_creates_skills_if_none(self):
        """测试 append_task_plan_agent 方法 - skills 为 None 时创建"""
        config = AgentConfig(plan_mode={"is_enabled": True}, skills=None)

        config.append_task_plan_agent()

        # Skills should be created
        assert config.skills is not None
        assert len(config.skills.agents) > 0

    def test_append_task_plan_agent_with_existing_skills(self):
        """测试 append_task_plan_agent 方法 - skills 已存在"""
        from app.domain.vo.agentvo.agent_config_vos import SkillVo

        config = AgentConfig(
            plan_mode={"is_enabled": True},
            skills=SkillVo(),  # Already a SkillVo instance
        )

        config.append_task_plan_agent()

        # Should add to existing skills
        assert config.skills is not None
        assert len(config.skills.agents) > 0


