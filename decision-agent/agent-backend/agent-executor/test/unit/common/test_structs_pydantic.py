class TestAgentInput:
    """测试 AgentInput Pydantic 模型"""

    def test_default_initialization(self):
        """测试默认初始化"""
        agent_input = AgentInput(query="test query")

        assert agent_input.query == "test query"
        assert agent_input.history is None
        assert agent_input.tool == {}
        assert agent_input.header == {}
        assert agent_input.self_config == {}

    def test_with_all_fields(self):
        """测试设置所有字段"""
        agent_input = AgentInput(
            query="test query",
            history=[{"role": "user", "content": "hello"}],
            tool={"name": "test_tool"},
            header={"auth": "token"},
            self_config={"key": "value"},
        )

        assert agent_input.query == "test query"
        assert len(agent_input.history) == 1
        assert agent_input.tool == {"name": "test_tool"}
        assert agent_input.header == {"auth": "token"}
        assert agent_input.self_config == {"key": "value"}

    def test_get_value_defined_field(self):
        """测试 get_value 方法 - 获取定义的字段"""
        agent_input = AgentInput(query="test")

        value = agent_input.get_value("query")

        assert value == "test"

    def test_get_value_undefined_field(self):
        """测试 get_value 方法 - 获取未定义的字段返回 None"""
        agent_input = AgentInput(query="test")

        value = agent_input.get_value("nonexistent_field")

        assert value is None

    def test_get_value_undefined_field_with_default(self):
        """测试 get_value 方法 - 获取未定义的字段返回默认值"""
        agent_input = AgentInput(query="test")

        value = agent_input.get_value("nonexistent_field", "default_value")

        assert value == "default_value"

    def test_get_value_extra_field(self):
        """测试 get_value 方法 - 获取额外字段"""
        agent_input = AgentInput(query="test", custom_field="custom_value")

        value = agent_input.get_value("custom_field")

        assert value == "custom_value"

    def test_set_value_defined_field(self):
        """测试 set_value 方法 - 设置定义的字段"""
        agent_input = AgentInput(query="initial")

        agent_input.set_value("query", "updated")

        assert agent_input.query == "updated"

    def test_set_value_new_field(self):
        """测试 set_value 方法 - 设置新字段"""
        agent_input = AgentInput(query="test")

        agent_input.set_value("new_field", "new_value")

        # Should be able to get the new value
        assert agent_input.get_value("new_field") == "new_value"

    def test_model_dump_with_empty_tool(self):
        """测试 model_dump 方法 - tool 为空时移除"""
        agent_input = AgentInput(query="test", tool={})

        dumped = agent_input.model_dump()

        assert "tool" not in dumped
        assert "query" in dumped

    def test_model_dump_with_tool(self):
        """测试 model_dump 方法 - tool 有值时保留"""
        agent_input = AgentInput(query="test", tool={"name": "test_tool"})

        dumped = agent_input.model_dump()

        assert "tool" in dumped
        assert dumped["tool"] == {"name": "test_tool"}

    def test_model_dump_with_extra_fields(self):
        """测试 model_dump 方法 - 包含额外字段"""
        agent_input = AgentInput(query="test", custom_field="custom_value")

        dumped = agent_input.model_dump()

        assert "custom_field" in dumped
        assert dumped["custom_field"] == "custom_value"

    def test_extra_fields_allowed(self):
        """测试允许额外字段"""
        # Should not raise validation error
        agent_input = AgentInput(
            query="test", custom_field1="value1", custom_field2="value2"
        )

        assert agent_input.custom_field1 == "value1"
        assert agent_input.custom_field2 == "value2"


