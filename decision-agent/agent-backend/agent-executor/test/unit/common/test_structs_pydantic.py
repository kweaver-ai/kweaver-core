class TestAgentConfig:
    """测试 AgentConfig Pydantic 模型"""

    def test_default_initialization(self):
        """测试默认初始化"""
        config = AgentConfig()

        assert config.input is None
        assert config.llms is None
        assert config.skills is not None  # SkillVo() due to validator
        assert config.data_source == {}
        assert config.system_prompt is None
        assert config.is_dolphin_mode is False
        assert config.dolphin is None
        assert config.pre_dolphin == []
        assert config.post_dolphin == []
        assert config.output == {}
        assert config.memory == {}
        assert config.related_question == {}
        assert config.plan_mode is None
        assert config.agent_id is None
        assert config.conversation_id is not None  # Auto-generated by validator
        assert config.session_id is None
        assert config.output_vars is None
        assert config.incremental_output is False

    def test_with_all_fields(self):
        """测试设置所有字段"""
        config = AgentConfig(
            input={"key": "value"},
            llms=[{"model": "gpt-4"}],
            skills=None,  # Will be converted to SkillVo()
            data_source={"type": "test"},
            system_prompt="Test prompt",
            is_dolphin_mode=True,
            dolphin="test_dolphin",
            pre_dolphin=[{"config": "value"}],
            post_dolphin=[{"config": "value2"}],
            output={"result": "value"},
            memory={"context": "data"},
            related_question={"question": "test"},
            plan_mode={"is_enabled": True},
            agent_id="agent_123",
            conversation_id="conv_456",
            session_id="session_789",
            output_vars=["var1", "var2"],
            incremental_output=True,
        )

        assert config.input == {"key": "value"}
        assert config.llms == [{"model": "gpt-4"}]
        assert config.skills is not None
        assert config.data_source == {"type": "test"}
        assert config.system_prompt == "Test prompt"
        assert config.is_dolphin_mode is True
        assert config.dolphin == "test_dolphin"
        assert config.pre_dolphin == [{"config": "value"}]
        assert config.post_dolphin == [{"config": "value2"}]
        assert config.output == {"result": "value"}
        assert config.memory == {"context": "data"}
        assert config.related_question == {"question": "test"}
        assert config.plan_mode == {"is_enabled": True}
        assert config.agent_id == "agent_123"
        assert config.conversation_id == "conv_456"
        assert config.session_id == "session_789"
        assert config.output_vars == ["var1", "var2"]
        assert config.incremental_output is True

